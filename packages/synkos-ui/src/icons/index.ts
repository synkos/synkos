let warned = false;
function warnMissing(name: string) {
  if (warned) return;
  // Only warn once per missing name to keep dev console quiet on repeated renders.
  warned = true;
  setTimeout(() => (warned = false), 0);
  console.warn(
    `[synkos-ui] missing icon "${name}" — falling back to "help_outline". ` +
      `Add the SVG via registerIcons({ ${name}: '<path d="..."/>' }).`
  );
}

/**
 * Resolve an icon's inner SVG by name. Unknown names fall back to a visible
 * `help_outline` glyph so typos surface immediately on the page (instead of
 * rendering an empty `<svg>`). In dev, missing names also log a one-shot
 * warning with the exact registration call.
 */
export function getIcon(name: string): string {
  const value = r[name];
  if (value) return value;
  // import.meta.env.DEV is true under Vite/Vitest; in other bundlers it's
  // undefined — so the warning is a no-op in production builds.
  const isDev =
    typeof import.meta !== 'undefined' && (import.meta as { env?: { DEV?: boolean } }).env?.DEV;
  if (isDev) warnMissing(name);
  return r.help_outline ?? '';
}

/**
 * Extend the icon catalog at runtime. Pass an object of name → inner SVG
 * (`<path d="..."/>`, `<g>...</g>`, …). Existing names are overwritten.
 *
 * @example
 * registerIcons({
 *   sparkle: '<path d="M12 2l2 7h7l-5.5 4 2 7-5.5-4-5.5 4 2-7L4 9h7z"/>',
 * })
 */
export function registerIcons(extra: Record<string, string>): void {
  Object.assign(r, extra);
}

