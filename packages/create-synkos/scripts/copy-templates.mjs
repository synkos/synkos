/**
 * Copies the root templates/ directory into this package so they are
 * bundled when published to npm. Run as part of the build step.
 *
 * Transformations applied:
 *   - .git/ dirs are skipped (templates are real repos during dev)
 *   - .quasar/ dirs are skipped (generated build cache)
 *   - Dotfiles that would affect npm pack are un-dotted:
 *       .gitignore    → gitignore
 *       .dockerignore → dockerignore
 *       .env.example  → env.example
 *       .npmrc        → npmrc
 *     scaffold.ts renames them back when copying to the user's project.
 *
 * Exclusions applied to the frontend template:
 *   - src-capacitor/ios/     (generated by `quasar build -m capacitor -T ios`)
 *   - src-capacitor/android/ (generated by `quasar build -m capacitor -T android`)
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT_TEMPLATES = path.resolve(__dirname, '../../../templates');
const DEST_TEMPLATES = path.resolve(__dirname, '../templates');

const EXCLUDED_DIRS = new Set([
  path.join('frontend', 'src-capacitor', 'ios'),
  path.join('frontend', 'src-capacitor', 'android'),
]);

const EXCLUDED_DIR_NAMES = new Set(['.git', '.quasar', '.claude', '.github', 'docs']);

const DOTFILE_RENAMES = {
  '.gitignore': 'gitignore',
  '.dockerignore': 'dockerignore',
  '.env.example': 'env.example',
  '.npmrc': 'npmrc',
};

function shouldExclude(relativePath) {
  for (const excluded of EXCLUDED_DIRS) {
    if (relativePath.startsWith(excluded)) return true;
  }
  return false;
}

function copyDir(src, dest, relativeBase = '') {
  fs.mkdirSync(dest, { recursive: true });
  for (const entry of fs.readdirSync(src, { withFileTypes: true })) {
    if (entry.isDirectory() && EXCLUDED_DIR_NAMES.has(entry.name)) continue;

    const relPath = path.join(relativeBase, entry.name);
    if (shouldExclude(relPath)) continue;

    const srcPath = path.join(src, entry.name);
    const destName = DOTFILE_RENAMES[entry.name] ?? entry.name;
    const destPath = path.join(dest, destName);

    if (entry.isDirectory()) {
      copyDir(srcPath, destPath, relPath);
    } else {
      fs.copyFileSync(srcPath, destPath);
    }
  }
}

// Clean and rebuild
if (fs.existsSync(DEST_TEMPLATES)) {
  fs.rmSync(DEST_TEMPLATES, { recursive: true });
}

copyDir(ROOT_TEMPLATES, DEST_TEMPLATES);

const count = fs.readdirSync(DEST_TEMPLATES, { recursive: true }).length;
console.log(`✓ Copied templates/ (${count} entries)`);
